    public ClsVoter VoterSearch(String voterId){
        
        for (ClsVoter voterSearch : this.voterGetList) {
            
            if(voterId.equals(voterSearch.getDocumentNumber())){
                
                return voterSearch;
                
            }
            
        }
        
        return null;
        
    }
